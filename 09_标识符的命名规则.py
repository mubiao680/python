# 变量命名规范
# 学完了标识符（变量、类、方法）的命名规则后，我们再来学习标识符的命名规范
# · 变量名
# · 类名
# · 方法名
# 不同的标识符，有不同的规范。
# 我们目前只接触到了：变量。所以，目前学习：变量的命名规范。
# · 见名知意
# · 下划线命名法
# · 英文字母全小写
#
# 变量命名规范 - 下划线命名法
# 多个单词组合变量名，要使用下划线做分隔。
first_number = 1
student_nickname = "小明"

# 变量命名规范 - 英文字母全小写
# 命名变量中的英文字母，应全部小写：

name = "张三"
age = 11