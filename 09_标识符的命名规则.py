# 标识符
# 在Python程序中：我们可以给很多东西起名字，比如：
# 变量的名字  ，方法的名字  ，类的名字，等等
# 这些名字，我们把它统一的称之为标识符，用来做内容的标识。
#
# 标识符：
# 是用户在编程的时候所使用的一系列名字，用于给变量、类、方法等命名。
#
# 标识符命名规则 - 内容限定
# 标识符命名中，只允许出现：
# 英文  ，中文  ，数字，下划线（_）
# 这四类元素
# 其余任何内容都不被允许。
#
# 注意
# 1.
# 不推荐使用中文
# 2.
# 且数字不可以开头
#
# 标识符命名规则 - 大小写敏感
# 以定义变量为例：

Andy = "安迪1"
andy = "安迪2"
print(Andy)
print(andy)
# 字母 a 的大写和小写，是完全能够区分的。

