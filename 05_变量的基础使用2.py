# 在print中输出多个变量以及做变量的基础运算
money = 50
name = "周杰轮"
# 我叫×××，兜里×××元
# print使用可以一次性在屏幕显示多份内容，格式：
# print(内容1, 内容2, …, 内容N)
# 内容是指：字面量或变量等
print("我叫", name, "，兜里", money, "元")
# 基本的数学运算，在Python中基本的数学加减乘除运算符号是：
print(50 + 5)
print(money + 10)


